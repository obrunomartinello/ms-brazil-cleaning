window.openContactModal = function (service) {
  const modal = document.getElementById('contact-modal');
  const container = document.getElementById('modal-container');
  if (!modal || !container) return;

  const activeLang = localStorage.getItem('ms_cleaning_lang') || 'en';
  const titleKey = `modal_title_${service}`;
  const translatedTitle = translations[activeLang][titleKey] || translations[activeLang]['modal_title_general'];
  
  document.getElementById('modal-title').textContent = translatedTitle;

  // Set URLs
  const msg = serviceMessages[activeLang][service] || serviceMessages[activeLang]['general'];
  const waUrl = `https://wa.me/18135457674?text=${encodeURIComponent(msg)}`;
  const smsUrl = `sms:+18135457674?body=${encodeURIComponent(msg)}`;

  document.getElementById('modal-wa-btn').href = waUrl;
  document.getElementById('modal-sms-btn').href = smsUrl;

  // Unhide and trigger animation
  modal.classList.remove('pointer-events-none');
  modal.classList.add('opacity-100');
  
  // Quick reflow to trigger scale transitions
  void container.offsetWidth;
  
  container.classList.remove('scale-95', 'opacity-0');
  container.classList.add('scale-100', 'opacity-100');
  
  // Disable body scroll when modal is open
  document.body.style.overflow = 'hidden';
};

window.closeContactModal = function () {
  const modal = document.getElementById('contact-modal');
  const container = document.getElementById('modal-container');
  if (!modal || !container) return;

  container.classList.remove('scale-100', 'opacity-100');
  container.classList.add('scale-95', 'opacity-0');

  modal.classList.remove('opacity-100');
  modal.classList.add('pointer-events-none');
  
  // Restore body scroll
  document.body.style.overflow = '';
};
